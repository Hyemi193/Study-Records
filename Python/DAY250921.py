# Unit 22.5 리스트 표현식 사용하기
# ------------------------------------------------------------------------------------------------------
## 리스트 컴프리헨션(list comprehension) : 리스트 안에 식, for 반복문, if 조건문 등을 지정하여 리스트를 생성하는 것

a = [i for i in range(10)]
print(a)

b = list(i for i in range(10))
print(b)

c = [i + 5 for i in range(10)]
print(c)

d = [i * 2 for i in range(10)]
print(d)

# 참고) 대괄호와 list() 리스트 표현식
## list 방식 : C언어
## [] 방식 : 파이썬

# 22.5.1 리스트 표현식에서 if 조건문 사용
a = [i for i in range(10) if i % 2 == 0]
print(a)

b = [i + 5 for i in range(10) if i % 2 == 1]
print(b)

# 22.5.2 for 반복문과 if조건문을 여러 번 사용
c = [i * j for i in range(2, 10) for j in range(1, 10)]
d = [i * j for i in range(2, 10)
           for j in range(1, 10)]
print(c)
print(d)

