## 메서드 : 객체에 속한 함수

# 22.1.1 리스트에 요소 추가하기
## append : 요소 하나를 추가
## extend : 리스트를 연결하여 확장
## insert : 특정 인덱스에 요소 추가

# 22.1.2 리스트에 요소 하나 추가하기
a = [10, 20, 30]
a.append(500)
print(a, len(a))

b = []
b.append(10)
print(b)

# 22.1.3 리스트 안에 리스트 추가하기
c = [10, 20, 30]
c.append([500, 600])
print(c, len(c))

# 22.1.4 리스트 확장하기
d = [10, 20, 30]
d.extend([500, 600])
print(d, len(d))

# 22.1.5 리스트의 특정 인덱스에 요소 추가하기
e = [10, 20, 30]
e.insert(2, 500)
print(e, len(e))

f = [10, 20, 30]
f.insert(0, 500)
print(f)

g = [10, 20, 30]
g.insert(len(g), 500)
print(g)

## 중첩 리스트
h = [10, 20, 30]
